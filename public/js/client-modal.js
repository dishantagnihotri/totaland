// Inject Modal HTML
function injectClientModal() {
    const modalHtml = `
    <div class="client-modal-overlay" id="clientModal">
        <div class="client-modal-content">
            <div class="modal-controls">
                <button class="modal-print" aria-label="Print modal" onclick="window.print()">
                    <img src="public/images/light_print.svg" alt="Print" />
                </button>
                <button class="modal-close" aria-label="Close modal">
                    <span style="font-size: 32px; color: #1C1C1C; font-weight: 300;">&times;</span>
                </button>
            </div>
            <div class="modal-header">
                <div class="modal-logo">
                    <img src="" alt="Client Logo" id="modalLogo" />
                </div>
                <div class="modal-title-area">
                    <h2 id="modalTitle"></h2>
                    <div class="modal-contact-info" id="modalContact">
                    </div>
                </div>
            </div>
            <div class="modal-description" id="modalDescription">
            </div>
        </div>
    </div>`;
    document.body.insertAdjacentHTML('beforeend', modalHtml);
}

// Initialize Modal Logic
function initClientModal() {
    injectClientModal();

    const modal = document.getElementById("clientModal");
    const modalLogo = document.getElementById("modalLogo");
    const modalTitle = document.getElementById("modalTitle");
    const modalContact = document.getElementById("modalContact");
    const modalDescription = document.getElementById("modalDescription");
    const closeModal = modal.querySelector(".modal-close");

    const openModal = (clientId) => {
        const data = clientData[clientId];
        if (data) {
            modalLogo.src = data.logo;

            // Hide title for Discovery
            if (clientId === 'discovery') {
                modalTitle.style.display = 'none';
            } else {
                modalTitle.style.display = 'block';
                modalTitle.textContent = data.title;
            }

            let contactHtml = "";
            // Removed website from header as it's now in the footer of the description
            if (data.contact) {
                contactHtml += `<div>${data.contact}</div>`;
            }
            modalContact.innerHTML = contactHtml;
            modalDescription.innerHTML = data.description;

            modal.classList.add("active");
            document.body.style.overflow = "hidden";
        }
    };

    const closeFunc = () => {
        modal.classList.remove("active");
        document.body.style.overflow = "";
    };

    // Use event delegation for client links
    document.addEventListener("click", function (e) {
        const link = e.target.closest(".client-link");
        if (link) {
            e.preventDefault();
            const clientId = link.getAttribute("data-client");
            openModal(clientId);
        }
    });

    closeModal.addEventListener("click", closeFunc);
    modal.addEventListener("click", (e) => {
        if (e.target === modal) closeFunc();
    });

    document.addEventListener("keydown", (event) => {
        if (event.key === "Escape" && modal.classList.contains("active")) {
            closeFunc();
        }
    });
}

// Run on DOM load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initClientModal);
} else {
    initClientModal();
}
